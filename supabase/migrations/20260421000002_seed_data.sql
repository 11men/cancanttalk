insert into public.questions (category_id, content, vote_count, yes_count, status)
select c.id, s.content, s.vote_count, s.yes_count, 'approved'::question_status
from seed s
join public.categories c on c.slug = s.slug
where not exists (
  select 1 from public.questions q where q.content = s.content
);
